"""CLI for importing legacy benchmark outputs into experiment metadata."""

from __future__ import annotations

import argparse
from pathlib import Path

from ai_data_scientist.experiments.ids import build_experiment_id
from ai_data_scientist.experiments.importer import import_legacy_experiment


def main(argv: list[str] | None = None) -> int:
    """Import legacy run outputs into normalized experiment metadata."""
    parser = argparse.ArgumentParser(
        description="Import legacy benchmark outputs into experiment metadata."
    )
    parser.add_argument(
        "--repo-root",
        type=Path,
        default=Path.cwd(),
        help="Repository root containing results/runs and results/configs.",
    )
    parser.add_argument(
        "--title",
        required=True,
        help="Human-readable title for the imported experiment.",
    )
    parser.add_argument(
        "--experiment-id",
        help="Stable experiment id. Defaults to an autogenerated id based on the title.",
    )
    parser.add_argument(
        "--description",
        help="Optional description stored in experiment metadata.",
    )
    parser.add_argument(
        "--config",
        dest="configs",
        action="append",
        help="Optional config filter. Repeat to include multiple configs.",
    )
    parser.add_argument(
        "--dataset",
        dest="datasets",
        action="append",
        help="Optional dataset filter. Repeat to include multiple datasets.",
    )
    args = parser.parse_args(argv)

    experiment_dir = import_legacy_experiment(
        repo_root=args.repo_root.resolve(),
        experiment_id=args.experiment_id or build_experiment_id(slug=args.title),
        title=args.title,
        description=args.description,
        configs=args.configs,
        datasets=args.datasets,
    )
    print(experiment_dir)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
